package com.company;

public class House{
    int Price,MeterSquare;

    public House(int price, int meterSquare) {
        Price = price;
        MeterSquare = meterSquare;
    }

    public int getPrice() {
        return Price;
    }

    public void setPrice(int price) {
        Price = price;
    }

    public int getMeterSquare() {
        return MeterSquare;
    }

    public void setMeterSquare(int meterSquare) {
        MeterSquare = meterSquare;
    }
}







